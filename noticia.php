              <p>O campus Salgueiro do IFSertãoPE realizou em 09 de setembro de 2021 o Sertão Maker: um evento online e gratuito sobre cultura maker, empreendedorismo e inovação. Um time de convidados de peso discutiu os desafios e vantagens de empreender no Brasil, e contaram um pouco das próprias experiências à frente de start ups e negócios de sucesso. As palestras foram transmitidas pelo YouTube.</p>

              <p>A primeira palestrante que abriu a programação foi Lúcia Helena Galvão, professora de filosofia há mais de 30 anos, que conquistou mais de 50 milhões de visualizações em seu canal no YouTube. Também tivemos a professora Débora Garofalo, primeira mulher brasileira e sul-americana a ser finalista no Global Teacher Prize, sendo considerada uma das 10 melhores professoras do mundo.</p>

              <p>Para compartilhar experiências sobre empreendedorismo no Brasil, tivemos mesas redondas com CEOs de duas start ups brasileiras, a CUBi Energia e a Hand Talk, que receberam aportes milionários nos últimos anos para seus projetos.</p>

