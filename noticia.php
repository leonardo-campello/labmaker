              <p>Tem dúvidas sobre como tudo isso funciona? Quer saber se vale apena investir no mercado de impressão 3D? Quer ouvir histórias de startups que já receberam milhões de reais e saíram de universidades federais aqui do Brasil? Quer aprender novas perspectivas sobre inovação e empreendedorismo?</p>
